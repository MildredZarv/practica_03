    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
           
        if(tipoExamen.isSelected(null)){
            JOptionPane.showMessageDialog(this, "Seleccione el tipo de examen.", "Mensaje", 1, null);
        }else{
            pa.setCarrera(String.valueOf(String.valueOf(ComboCarrera.getSelectedItem())));
            pa.setMateria(String.valueOf(ComboMaterias.getSelectedItem()));
            pa.setSemestre(Integer.parseInt(String.valueOf(semestre.getValue())));
            String t="";{
            if(Prueba.isSelected()){
                t="Prueba";
            }else{
                t="Real";
            }
        }
            pa.setTipoExamen(t);
        ResponderExamen re=new ResponderExamen();
        re.setpgt(pa);
        re.setVisible(true);
        setVisible(false);
        }
    }//GEN-LAST:event_AceptarActionPerformed
    
